import { describe, expect, it } from "vitest";
import { readSetList } from "../document";

/**
 * PTB7 payload from a real package that starts with a nameless placeholder set
 * (titleLen === 0). The old parser treated that as end-of-list and returned [].
 */
const JACK_BRITT_PTB7_HEAD = Uint8Array.from([
    0x00, 0x01, 0x00, 0x14, 0x00, 0x04, 0x4a, 0x43, 0xeb, 0x5a, 0xce, 0x33,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x01, 0x00, 0x01, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x04, 0x4a, 0x44, 0x35, 0xa8, 0x02, 0x6c, 0x00,
    0x18, 0x00, 0x00, 0x00, 0x00, 0x03, 0x31, 0x2d, 0x38, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x01, 0x00, 0x01,
    0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x23, 0x0b, 0xd8, 0xfd, 0x6d,
    0xd0, 0x00, 0x30, 0x00, 0x00, 0x00, 0x00, 0x04, 0x39, 0x2d, 0x31, 0x36,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x01, 0x00, 0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x04, 0xb1, 0x3c,
    0x05, 0xb8, 0x3a, 0x04, 0x00, 0x38, 0x00, 0x00, 0x00, 0x00, 0x05, 0x31,
    0x37, 0x2d, 0x31, 0x38, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x01, 0x00, 0x01, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x04, 0x4a, 0x47, 0xf1, 0x56, 0x52, 0xd1, 0x00, 0x40, 0x00, 0x00,
    0x00, 0x00, 0x05, 0x31, 0x39, 0x2d, 0x32, 0x30, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x01, 0x00, 0x01, 0x00,
]);

describe("readSetList", () => {
    it("reads named sets after a nameless placeholder", () => {
        const sets = readSetList(JACK_BRITT_PTB7_HEAD);
        expect(sets.length).toBeGreaterThanOrEqual(4);
        // The leading record is the count-0 opening formation; the old parser
        // treated its empty title as end-of-list and returned [].
        expect(sets[0]).toMatchObject({ name: "", cumulativeCount: 0 });
        expect(sets[1]).toMatchObject({ name: "1-8", cumulativeCount: 24 });
        expect(sets[2]).toMatchObject({ name: "9-16", cumulativeCount: 48 });
        expect(sets[3]).toMatchObject({ name: "17-18", cumulativeCount: 56 });
    });

    it("reads the trailer subset marker per set", () => {
        const sets = readSetList(JACK_BRITT_PTB7_HEAD);
        // The flag marks the set it sits on: "1-8" is the labeled hold, and it
        // is the one the source's own on-field "HOLD" text box annotates.
        expect(sets[1]).toMatchObject({ name: "1-8", isSubset: true });
        expect(sets[2]).toMatchObject({ name: "9-16", isSubset: false });
    });

    it("returns empty for a truncated payload", () => {
        expect(readSetList(new Uint8Array([0x00, 0x01]))).toEqual([]);
    });
});
